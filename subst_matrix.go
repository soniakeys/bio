func parseSubstMatrix(s string) *SubstMatrix {
	b := bytes.NewBufferString(s)
	a := make([]byte, 20)
	var aa string
	for i := range a {
		fmt.Fscan(b, &aa)
		a[i] = aa[0]
	}
	m := make([][]int, 20)
	for i := range m {
		fmt.Fscan(b, &aa)
		mi := make([]int, 20)
		for j := range mi {
			fmt.Fscan(b, &mi[j])
		}
		m[i] = mi
	}
	return NewSubstMatrix(a, m)
}
