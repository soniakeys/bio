package bio

type SubstMatrix struct {
	Alphabet []byte
	Matrix   [][]int // len(Alphabet)*len(Alphabet)
	Index    []int   // lookup table: Alphabet[Index[symbol]] = symbol
}

// Score satisfies Aligner interface.
func (s *SubstMatrix) Score(x, y byte) int {
	return s.Matrix[s.Index[x]][s.Index[y]]
}

// NewSubstMatrix creates a new SubstMatrix.
func NewSubstMatrix(a []byte, m [][]int) *SubstMatrix {
	var max byte
	for _, b := range a {
		if b > max {
			max = b
		}
	}
	x := make([]int, max+1)
	for i, b := range a {
		x[b] = i
	}
	return &SubstMatrix{a, m, x}
}

